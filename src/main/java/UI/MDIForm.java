    private void req_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_req_SearchActionPerformed

        try {

            SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd");
            String wardName = (String) requestWard.getSelectedItem();
            String sql = "{call RequestSearch('" + wardName + "')}";
            Database_Connection dbConnection = Database_Connection.GetInstance();
            ResultSet result = dbConnection.getData(sql);
            ResultSetMetaData md = result.getMetaData();
            int columnCount = md.getColumnCount();
            Vector columns = new Vector(columnCount);
            for (int i = 1; i <= columnCount; i++) {
                columns.add(md.getColumnName(i));
            }

            Vector data = new Vector();
            Vector row;
            while (result.next()) {

                row = new Vector(columnCount);
                row.add(String.valueOf(result.getString("REQUEST_ID")));
                row.add(result.getString("REQUEST_TYPE"));
                row.add(df.format(result.getDate("REQUEST_DATE")));
                row.add(result.getString("REQUEST_SHIFT"));
                row.add(result.getString("WARD_NAME"));
                row.add(String.valueOf(result.getString("NURSE_ID")));

                data.add(row);
            }
            DefaultTableModel tableModel = new DefaultTableModel(data, columns);
            requestTable.setModel(tableModel);

        } catch (Exception ex) {
            Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                Database_Connection.closeConnection();
            } catch (SQLException ex) {
                Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_req_SearchActionPerformed

